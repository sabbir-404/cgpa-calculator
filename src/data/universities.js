export const getGradePoint = (university, grade) => {
  const uni = universities[university];
  if (!uni) return null;
  const gradeData = uni.grades.find((g) => g.letter === grade);
  return gradeData ? gradeData.point : null;
};

export const getGradeFromMarks = (university, marks) => {
  const uni = universities[university];
  if (!uni) return null;
  const gradeData = uni.grades.find(
    (g) => marks >= g.minMarks && marks <= g.maxMarks
  );
  return gradeData ? gradeData.letter : null;
};
